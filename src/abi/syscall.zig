}

const __max_invalid = 170;
const _invalid: [__max_invalid]u32 = blk: {
    var vals: [__max_invalid]u32 = undefined;
    for (0..__max_invalid) |i| vals[i] = std.math.maxInt(u32) - i - 1;
    break :blk vals;
};

