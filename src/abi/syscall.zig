//! Linux syscall numbers, one enum covering all four architectures.
//!
//! Sourced from the Linux kernel source tree, torvalds/linux @
//! 8cdeaa50eae8dad34885515f62559ee83e7e8dda (kernel version 7.2.0-rc2):
//!
//! | arch | source file | scope |
//! | --- | --- | --- |
//! | x86_64 | `arch/x86/entry/syscalls/syscall_64.tbl` | "common" and "64" ABI rows ("x32" rows excluded) |
//! | aarch64 | `include/uapi/asm-generic/unistd.h` | the generic table aarch64 uses directly, with the `__BITS_PER_LONG==64` (non-compat) branch's `__NR3264_*` remapping applied (e.g. `mmap`, `fcntl`, `newfstatat` resolve to their 64-bit-named numbers, not the `mmap2`/`fcntl64`-style 32-bit compat names) |
//! | arm | `arch/arm/tools/syscall.tbl` | "common" and "eabi" rows ("oabi" rows excluded -- legacy OABI, not the EABI target this project builds) |
//! | powerpc | `arch/powerpc/kernel/syscalls/syscall.tbl` | "common", "nospu", and "32" rows ("64"-only and "spu"-only rows excluded -- this project targets 32-bit, non-Cell/SPU powerpc) |
//!
//! Calling conventions (number register / arg registers / return register):
//!
//! | arch | instruction | number | args | return |
//! | --- | --- | --- | --- | --- |
//! | x86_64 | `syscall` | `rax` | `rdi`, `rsi`, `rdx`, `r10`, `r8`, `r9` (note `r10`, not `rcx` -- `rcx` is clobbered by `syscall` itself) | `rax` |
//! | aarch64 | `svc #0` | `x8` | `x0`-`x5` | `x0` |
//! | arm | `svc #0` (EABI; older OABI used `swi`) | `r7` | `r0`-`r5` | `r0` |
//! | powerpc | `sc` | `r0` | `r3`-`r8` | `r3` (carry flag conventionally signals error vs. success, unlike the other three arches) |
//!
//! A syscall's number *and* its very presence differ per arch throughout the
//! whole table.  For example, aarch64 dropped plain `open`/`stat`/`unlink`
//! in favor of the `*at()` forms only, so no single architecture defines every
//! name below. `pub const Number` is therefore one flat enum over the
//! *union* of all names any of the four architectures define, where each name's
//! value is picked per architecture by a `builtin.cpu.arch` switch.
//!
//! A name a given architecture's kernel does NOT define resolves, on that
//! architecture, to one of the `_invalid[N]` sentinel values below
//! (`std.math.maxInt(u32) - N - 1`) rather than a real Linux syscall number.
//! This is deliberate, not a numbering guess: real Linux syscall numbers
//! never come close to using the top bit of a `u32` (the largest table here
//! has under 500 entries), so every sentinel is trivially distinguishable
//! from every real number by that bit alone. A syscall dispatcher can
//! therefore treat a 1 in the topmost bit as invalid.
//! This has the unfortunate effect of making one of these invalid syscalls a
//! runtime check rather than a compile error, but since most code is truly
//! going to link to real Linux headers instead of ours, that can act as a
//! proxy safeguard.
//!
//! Each arch's sentinel indices (the `N` in `_invalid[N]`) are assigned
//! sequentially and independently per architecture, not globally -- e.g.
//! `_invalid[5]` means something different when evaluated for aarch64 than
//! for arm. This is safe because only one architecture's switch branches
//! are ever evaluated in a given build (the non-taken branches for the
//! other three architectures are never analyzed), so the only collision
//! that matters is *within* one architecture's resolved set of values, and
//! each architecture's assignment is a bijection over its own missing names.

